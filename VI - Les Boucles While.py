"""
Une alternative à l'instruction for couramment utilisée en informatique est la boucle while. 
Avec ce type de boucle, 
    une série d'instructions est exécutée tant qu'une condition est vraie. 
"""

# Par exemple :

i = 1
while i <= 4:
    print(i)
    i = i + 1
